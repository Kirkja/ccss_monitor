<h3>Bloom's Taxonomy Guide</h3>
<div class="lessonArea">
    <p>In the  bullet list below are the descriptors for Bloom's Taxonomy for each of the major levels.  Under the revised version, there are also sublevels to each major level of Blooms.  These are not included since they are not being used currently in the review tool.</p>
    <ul>
        <li><b>BLM-0 (Non-Cognitive):</b> No academic rigor involved.</li>
        <li><b>BLM-1 (Remembering):</b> Retrieving, recognizing, and recalling relevant knowledge from long-term memory.</li>
        <li><b>BLM-2 (Understanding):</b> Constructing meaning from oral, written, and graphic messages through interpreting, exemplifying, classifying, summarizing, inferring, comparing, and explaining.</li>
        <li><b>BLM-3 (Applying):</b> Carrying out or using a procedure through executing, or implementing.</li>
        <li><b>BLM-4 (Analyzing):</b> Breaking material into constituent parts, determining how the parts relate to one another and to an overall structure or purpose through differentiating, organizing, and attributing.</li>
        <li><b>BLM-5 (Evaluating):</b> Making judgments based on criteria and standards through checking and critiquing.</li>
        <li><b>BLM-6 (Creating):</b> Putting elements together to form a coherent or functional whole; reorganizing elements into a new pattern or structure through generating, planning, or producing.</li>
    </ul>  



