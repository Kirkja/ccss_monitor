<div class="lessonArea">
    <img class="imageAreaRight" src="<?php echo base_url(); ?>css/images/topic_02_a.png"/>
    <p>The "Open Work" panel contains work assignment folders. Within each folder is a collection of samples needing to be reviewed. 
        If you look at the folder names, you will notice most are hyphenated.  To the left of the hyphen is typically the subject 
        area for the samples. English or Math for example.  To the right of the hyphen is the grade level (as a number) from which 
        the sample was collected. So a "1" would be first grade while a "0" would be kindergarten.  Occasionally, you'll see a grade level 
        that is larger than "100".  This is a discrete course, like "English Lit" at the high school.    
    </p>
    <p class='noted'>
        To open a specific folder, click on the little folder icon at the left of the folder's name.
    </p>
    
    <p>
        Once a folder has been opened, you will see a list of individual samples.  The sample name is a unique code composed 
        of letters and numbers followed by a hyphen and another number.  The appended number represents a page number 
        for the assignment. While most samples have a single page (hence the - 1), some may have more than one page. In this 
        case, you would see something like "HY7K9OAX-1" followed by a "HY7K9OAX-2".  In general, this is simply a means to identify 
        samples and their respective pages when the need arises.
    </p>




