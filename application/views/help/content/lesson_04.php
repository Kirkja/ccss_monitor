    <p>When a sample is currently selected, a collection of standards are available for browsing 
        can be found in the "<b>Standards Tab</b>" located in the center region of the workspace.</p>
    
    <p>In most cases, this is a single set; however, multiple standards may be listed depending 
        on the project.  Simply click on the desired set of standards and its full listing will 
        appear automatically.                
    </p>    
    
    <p class="noted">To select a standard as a review item, click on the button which starts the standard description.</p>
    
    <p>Each standard has a button with its name within it.  Clicking on these buttons will inject 
        that standard as a review item for the sample.  There is no limit to the number of standards 
        that may be injected this way.</p>
    
