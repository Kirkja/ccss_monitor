<h3>Topic 06: Working with Review Items</h3> 
<div class="lessonArea">
    
    <img src="<?php echo base_url(); ?>css/images/topic_06_a.jpg" class="imageAreaRight" />
    
    <p>The "<b>Review Items</b>" panel represent to most feature rich panel in the entire workspace. This panel is 
        where are the review data for a sample is located.  Using the image to the right as an illustration, 
        we'll discuss this panel in some detail.  Please note that all actions performed within the "Review Items" panel are saved automatically.
    </p>
    
    
    <p>
        Beneath the "Review Items" title bar of the panel are two buttons.
    </p>
    
    <ol>
        <li><b>Del SCR:</b> This button will delete selected (using the check boxes) review items from this sample.</li>
        <li><b>Add SCR:</b> This button will inject a blank review item into this sample.</li>
    </ol>
    
    <p>The "SCR" portion of those labels refers to "Standards and Cognitive Rigor" which is the focus of these review items.</p>
    
    <p class='noted'>To delete a row, check its box, then click on the "Del SCR" button.</p>
    
    <p>Deleting rows is permanent so be careful when checking boxes and performing delete actions. You may delete as many rows at once as are checked.</p>
    
    
    <p class='noted'>To add a blank review item, click on the "Add SCR" button.</p>
    
    <p>When using this method, there will be no standard tied to the review item. You will need to manually 
        type in the standard. The system will test your entry against all available standards. Only valid 
        standards will be accepted using this method. So you will need to be careful.</p>
    
    <p>Looking at the rows themselves, each one is composed of six parts. From left to right across the row:</p>
    
    <ol>
        <li>Check box: This is used to select rows for deletion.</li>        
        <li>Problem Number: The problem number off the page for this item.</li>        
        <li>Standard: The official standard of this item.</li>
        <li>DOK: The Depth of Knowledge of this item.</li>        
        <li>BLM: The Bloom's Taxonomy level of item.</li>        
        <li>Count: The number of times the combination appears on the sample.</li>        
    </ol>
    
    <p class='noted'>To enter a problem number, click into the input box and type the problem number.</p>

    <p>This problem number input box will accept any text entries. Items like "3", "3.1", "3.1.A", or "A-1" are all valid.</p>
    
    <p class='noted'>To manually enter a standard, type the standard code into the input box.</p>
    
    <p>As noted earlier, this field requires that the standard being entered must be valid.  
        An error message will appear if the entry is not valid and this entry will not be saved 
        into the system. If you are planning to use this method instead of having the standards 
        injected from the full listings or the search feature results, please follow the pattern.  
        You will notice that all of the standards start with the state abbreviation then an underscore 
        followed by the grade level and then the normal standard's nomenclature. <i>This may not be what 
        you would normally see when looking at the official standard documents</i>.</p>
    
    <p class='noted'>To set the "DOK" or "BLM", simply use the appropriate drop down menu.</p>
    
    <p>Please make a selection for each of these drop downs.  The default "DOK-?" or "BLM-?" 
        will be ignored as it is just a place holder for blank entries.  You may use the "DOK-0" 
        or "BLM-0" if the item has no cognitive value.  Please refer to the Blooms or Depth of Knowledge 
        reference guides if you need help with those terms.</p>
    
    <p class="noted">To write a note about the review item, click on the little pen and pad icon at the far right of the row.</p>

     
    <p>If a note exits for a given row, then the icon will be green in color; otherwise, a black icon indicates that 
        no note exists for that item. If you click the icon, the row will highlight (in yellow) and any note available 
        will appear in the "Review Item Note" panel below the "Review Items" panel. You can make whatever entries you need. 
        This text box accepts most characters and can be completely deleted if desired.</p>
    
    <img class="imageAreaRight" src="<?php echo base_url(); ?>css/images/topic_06_b.jpg" />
   
    <p class="noted">To delete a note, click the "Delete" button while a note is activate.</p>
    
    <p>Deleted notes are permanently removed from the system.</p>
    
    <p class="noted">To save a note, click the "Save" button when the note is active.</p>
    
    <p>You must always save a note to ensure that after entering or editing it.  Otherwise the note will be lost. </p>
    
</div>