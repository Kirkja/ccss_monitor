    <h3>Topic 01: Introduction to the workspace</h3> 
    <div class="lessonArea">
        <img class="imageAreaRight" src="<?php echo base_url(); ?>css/images/topic_01_a.png" />
        
        <p>The analyst's workspace is divided into regions within the browser screen. 
            Each region has a particular set of functionality. For ease of discussion, 
            each of these regions has been lightly shaded with a color.</p>
        
        <div >
            <p class="golden">At the top of the screen (golden tinted area), is the title region.  
                This region has only a few features:</p>
            <ol>
                <li>In the top row are both the user welcome message (on the left) and the logout button on the right.</li>
                <li>In the middle row are are a series of contextual "Tabs", like the "Analysis" tab.</li>
                <li>In the bottom row are a series of contextual buttons, like the "Help" button on the right.</li>
            </ol>
        </div>
    
        <div>
            <p class="azure">On the left hand side of the screen (blue tinted area), is the sample navigation 
                region. This region has an upper panel ("<i>Open Work</i>") and a lower panel 
                ("<i>Completed Work</i>".)  Both panels contain folders which hold review samples.</p>
                
            <ol>
                <li>Open Work: Folders here are your current unfinished assignments.</li>
                <li>Completed Work: Folders here are your completed assignments; ie, you marked them as such.</li>
            </ol>
        </div>
   
        <div>
            <p class="greenish">In the center of the screen (green tinted area), is an area with three tabs. 
                These tabs provide access to features used in making your review of the selected sample.                
            </p>
                
            <ol>
                <li>Sample: This tab shows an image of the sample to be reviewed.</li>
                <li>Standards: This tab provides a list of potential standards to be used for the review. 
                The selected set of standards (as a full listing) will automatically appear for you.</li>
                <li>Search: This tab give you access to a search feature which can help narrow down 
                    your choice of standards to use in the review.</li>
            </ol>
        </div>        
        <div>
            <p class="pinkish">On the right of the screen (red tinted area), is the review region. This region 
                has an upper panel ("<i>Review Items</i>") and a lower panel ("<i>Review Item Note</i>").
            </p>
                
            <ol>
                <li>Review Items: This panel holds all your review data for the selected sample. Each review item is 
                    grouped as a single row composed of other sub-items: like, a standard, its Bloom's Taxonomy and 
                    depth of knowledge for example.</li>
                <li>Review Item Note: This panel gives you a simple note taking feature for the selected review item in the upper panel.</li>
            </ol>
        </div>    
    </div>


