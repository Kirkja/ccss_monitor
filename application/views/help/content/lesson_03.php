<h3>Topic 03: Working with sample images</h3> 
<div class="lessonArea">
    
    <img src="<?php echo base_url(); ?>css/images/topic_03_a.jpg" class="imageAreaRight" />
    <p>When a sample is currently selected, an image of that sample can be viewed in the 
        "<b>Sample Tab</b>" located in the center region of the workspace.        
    </p>    
    
    <p>The "Sample Tab" has several features to make viewing images more convenient. These are located 
        just above the image as a series of buttons:</p>
    
    <ol>
        <li><b>Rotate CCW:</b> Will rotate the image counter-clock-wise.</li>
        <li><b>Rotate CW:</b> Will rotate the image clock-wise.</li>
        <li><b>Blank</b>: Will mark the image as a blank and remove it from the system.</li>
        <li><b>Detach:</b> Will open a new window exclusively for the image.  Detached 
            images also have the rotation features, plus a "Fit Window" ability.</li>
    </ol>
        
</div>