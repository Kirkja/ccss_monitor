<h3>Topic 04: Working with Standards</h3> 
<div class="lessonArea">
    
    <img src="<?php echo base_url(); ?>css/images/topic_04_a.jpg" class="imageAreaRight" />
    
    <p>When a sample is currently selected, a catalog of standards available for browsing 
        can be found in the "<b>Standards Tab</b>" located in the center region of the workspace.</p>
    
    <p>In most cases, this is a single set of standards; however, multiple standards may be listed depending 
        on the project. Standards typically have names such as: "<i>CA CCSS English Language Arts</i>" which 
        would be California's variant on the Common Core State Standards for English Language Arts.       
    </p>  
    
    <p class='noted'>
        To choose a set of standards from the catalog, click on its name or the radio button in front of its name.
    </p>
    
    <p>
        Once a set of standards has been chosen, the full listing for that set will appear below in a scrollable area.
    </p>
    
    <p class="noted">To select a particular standard as a review item, click on the button which starts the 
        standard's description.</p>
    
    <p>Each standard has a button with its name within it.  Clicking on these buttons will inject 
        that standard as a review item for the sample.  These injected standards will appear in the "<i>Review Items</i>" panel. 
        There is no limit to the number of standards that may be injected this way.</p>
    
